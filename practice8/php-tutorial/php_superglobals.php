    <h2>PHP Global Variables - Superglobals</h2>
    <p>Some predefined variables in PHP are &quot;superglobals&quot;, which means that they
        are always accessible, regardless of scope - and you can access them from any
        function, class or file without having to do anything special.</p>
    <p>The PHP superglobal variables are:</p>
    <ul>
        <li>$GLOBALS</li>
        <li>$_SERVER</li>
        <li>$_REQUEST</li>
        <li>$_POST</li>
        <li>$_GET</li>
        <li>$_FILES</li>
        <li>$_ENV</li>
        <li>$_COOKIE</li>
        <li>$_SESSION</li>
    </ul>
